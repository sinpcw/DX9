#include"Owl.hpp"

//***********************************************************************************************************************************************************************
//		UI::UI()
//***********************************************************************************************************************************************************************
owl::UI::UI() : owl::engine::GRAPH9(), owl::engine::INPUT8(), owl::engine::AUDIO8() {
	//-----	no Action
}

//***********************************************************************************************************************************************************************
//		UI::~UI()
//***********************************************************************************************************************************************************************
owl::UI::~UI(){
	Quit();
}

//***********************************************************************************************************************************************************************
//		UI::Init(const core::VECT2i &, const wchar_t *, const wchar_t *, const wchar_t *)
//***********************************************************************************************************************************************************************
bool owl::UI::Init(const core::VECT2i &_Size, const wchar_t *_Title, const wchar_t *_Icon, const wchar_t *_Menu){
	if(false == engine::GRAPH9::Init(_Size, _Title, _Icon, _Menu)){
		return (false);
	}
	if(false == engine::INPUT8::Init(GetWindowPtr())){
		return (false);
	}
	if(false == engine::AUDIO8::Init(GetWindowPtr())){
		return (false);
	}
	return (true);
}

//***********************************************************************************************************************************************************************
//		UI::Init(const core::VECT2i &, int, int, const wchar_t *, const wchar_t *)
//***********************************************************************************************************************************************************************
bool owl::UI::Init(const core::VECT2i &_Size, int _Windowed, int _DWM, const wchar_t *_Title, const wchar_t *_Icon){
	if(false == engine::GRAPH9::Init(_Size, _Windowed, _DWM, _Title, _Icon)){
		return (false);
	}
	if(false == engine::INPUT8::Init(GetWindowPtr())){
		return (false);
	}
	if(false == engine::AUDIO8::Init(GetWindowPtr())){
		return (false);
	}
	return (true);
}

//***********************************************************************************************************************************************************************
//		UI::Quit()
//***********************************************************************************************************************************************************************
bool owl::UI::Quit(){
	engine::AUDIO8::Quit();
	engine::INPUT8::Quit();
	engine::GRAPH9::Quit();
	return (true);
}

//***********************************************************************************************************************************************************************
//		UI::Ctrl()
//***********************************************************************************************************************************************************************
bool owl::UI::Ctrl(){
	engine::AUDIO8::Ctrl();
	engine::INPUT8::Ctrl();
	return engine::GRAPH9::Ctrl();
}
